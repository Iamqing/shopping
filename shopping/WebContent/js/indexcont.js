// JavaScript Document
function $(elementid) {
	return document.getElementById(elementid);
}
function chgborder(obj) {
	obj.style.border="#f00 1px dashed";
	obj.firstElementChild.lastElementChild.style.display="block";
}
function resetborder(obj) {
	obj.style.border="#DBEBBC 1px solid";
	obj.firstElementChild.lastElementChild.style.display="none";
}